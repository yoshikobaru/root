const server = https.createServer(options, async (req, res) => {
  const parsedUrl = url.parse(req.url, true);
  const pathname = parsedUrl.pathname;
  const method = req.method;

  // Ð”Ð¾Ð±Ð°Ð²Ð»ÑÐµÐ¼ ÑÐ¿ÐµÑ†Ð¸Ð°Ð»ÑŒÐ½ÑƒÑŽ Ð¿Ñ€Ð¾Ð²ÐµÑ€ÐºÑƒ Ð´Ð»Ñ API ÑÐ½Ð´Ð¿Ð¾Ð¸Ð½Ñ‚Ð°
  if (pathname.startsWith('/user/public/')) {
    try {
      const telegramId = pathname.split('/').pop();
      
      if (!telegramId) {
        res.writeHead(400, { 'Content-Type': 'application/json' });
        res.end('false');
        return;
      }

      const exists = await User.count({ 
        where: { telegramId }
      }) > 0;

      res.writeHead(200, { 
        'Content-Type': 'application/json',
        'Access-Control-Allow-Origin': '*'
      });
      res.end(JSON.stringify(exists));
      return;
    } catch (error) {
      console.error('Error checking user:', error);
      res.writeHead(500, { 'Content-Type': 'application/json' });
      res.end('false');
      return;
    }
  }

  // ÐŸÑ€Ð¾Ð²ÐµÑ€ÑÐµÐ¼ rate limit Ñ‚Ð¾Ð»ÑŒÐºÐ¾ Ð´Ð»Ñ Ð¾Ð¿Ñ€ÐµÐ´ÐµÐ»ÐµÐ½Ð½Ñ‹Ñ… ÑÐ½Ð´Ð¿Ð¾Ð¸Ð½Ñ‚Ð¾Ð²
  if (LIMITED_ENDPOINTS.includes(pathname)) {
    const rateLimitError = await rateLimitMiddleware(req);
    if (rateLimitError) {
      res.writeHead(rateLimitError.status, { 
        'Content-Type': 'application/json',
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Headers': 'Content-Type, X-Telegram-Init-Data'
      });
      res.end(JSON.stringify(rateLimitError.body));
      return;
    }
  }

  // ÐŸÑ€Ð¾Ð²ÐµÑ€ÑÐµÐ¼ ÑÑƒÑ‰ÐµÑÑ‚Ð²Ð¾Ð²Ð°Ð½Ð¸Ðµ Ñ€Ð¾ÑƒÑ‚Ð° Ð² routes
  if (routes[method]?.[pathname]) {
    try {
      const handler = routes[method][pathname];
      const result = await handler(req, res, parsedUrl.query);
      
      res.writeHead(result.status, { 
        'Content-Type': 'application/json',
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Headers': 'Content-Type, X-Telegram-Init-Data'
      });
      
      res.end(JSON.stringify(result.body));
      return;
    } catch (error) {
      console.error('Route handler error:', error);
      res.writeHead(500, { 'Content-Type': 'application/json' });
      res.end(JSON.stringify({ error: 'Internal Server Error' }));
      return;
    }
  }

  // ÐžÐ±Ñ€Ð°Ð±Ð¾Ñ‚ÐºÐ° ÑÑ‚Ð°Ñ‚Ð¸Ñ‡ÐµÑÐºÐ¸Ñ… Ñ„Ð°Ð¹Ð»Ð¾Ð²
  if (isStaticRequest(pathname)) {
    let filePath = path.join(__dirname, 'dist', pathname);
    
    // ÐšÐµÑˆÐ¸Ñ€ÑƒÐµÐ¼ Ñ‚Ð¾Ð»ÑŒÐºÐ¾ Ñ…ÐµÑˆÐ¸Ñ€Ð¾Ð²Ð°Ð½Ð½Ñ‹Ðµ Ð°ÑÑÐµÑ‚Ñ‹
    if (isHashedAsset(pathname)) {
      res.setHeader('Cache-Control', 'public, max-age=31536000, immutable');
    } else {
      res.setHeader('Cache-Control', 'no-store, must-revalidate');
      res.setHeader('Pragma', 'no-cache');
    }
    
    serveStaticFile(filePath, res);
    return;
  }

  // Ð•ÑÐ»Ð¸ Ð½Ðµ ÑÑ‚Ð°Ñ‚Ð¸Ñ‡ÐµÑÐºÐ¸Ð¹ Ñ„Ð°Ð¹Ð» Ð¸ Ð½Ðµ API route, Ð²Ð¾Ð·Ð²Ñ€Ð°Ñ‰Ð°ÐµÐ¼ index.html
  let filePath = path.join(__dirname, 'dist', 'index.html');
  res.setHeader('Cache-Control', 'no-store, must-revalidate');
  res.setHeader('Pragma', 'no-cache');
  serveStaticFile(filePath, res);
});

